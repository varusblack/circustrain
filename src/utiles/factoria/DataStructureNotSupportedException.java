package utiles.factoria;

@SuppressWarnings("serial")
public class DataStructureNotSupportedException extends java.lang.RuntimeException {

	public DataStructureNotSupportedException() {
		super();
	}

	public DataStructureNotSupportedException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataStructureNotSupportedException(String message) {
		super(message);
	}

	public DataStructureNotSupportedException(Throwable cause) {
		super(cause);
	}

	
}
